frase9 = 'Curso em video python'
#O len conta a quantidade de caraacteres de ma frase(string)
print(len(frase9))
#O .count conta quantas vezes uma determinada letra aparece na frase
print(frase9.count('o'))
# O find conta quantas vezes uma determinada silaba aparece na frase
print(frase9.find('python'))
# O operador in verifica se a palavra especificada consta na frase e retorna true ou false
print('Curso' in frase9)
# O .replace realiza a troca de uma palavra por outra
print(frase9.replace('python','android'))
# O .upper Escreve a frase em maiusculo
print(frase9.upper())
# O .lower Escreve a frase em minusculo
print(frase9.lower())
# O .capitalize deixa a primeira letra da frase em maiusculo
print(frase9.capitalize())
print(frase9.split())
    